#!/usr/bin/env node

const fs = require('fs');
const path = require('path');
const dotenv = require('dotenv');
const { upsertEnvVar, triggerRedeployment, hasRailwayCli } = require('../lib/railway-helpers');

dotenv.config({ path: path.resolve(process.cwd(), '.env.local') });
dotenv.config();

const APP_BASE_URL = (process.env.APP_BASE_URL || `https://${process.env.RAILWAY_PUBLIC_DOMAIN || 'freedomforge-max.up.railway.app'}`).replace(/\/$/, '');
const LOOKBACK_HOURS = Math.max(6, Number(process.env.CASHFLOW_TUNE_LOOKBACK_HOURS || 24));
const LOG_LIMIT = Math.max(500, Number(process.env.CASHFLOW_TUNE_LOG_LIMIT || 4000));
const AUTO_REDEPLOY = String(process.env.CASHFLOW_TUNE_AUTO_REDEPLOY || 'false').toLowerCase() === 'true';
const DRY_RUN = String(process.env.DRY_RUN || 'false').toLowerCase() === 'true';
const OUTPUT_PATCH_FILE = process.env.CASHFLOW_TUNE_OUTPUT_FILE || 'ops/cashflow-autotune.latest.env';
const CIRCUIT_BREAKER_ENABLED = String(process.env.CASHFLOW_CIRCUIT_BREAKER_ENABLED || 'true').toLowerCase() !== 'false';
const CIRCUIT_FAIL_SPIKE = Math.max(1, Number(process.env.CASHFLOW_CIRCUIT_FAIL_SPIKE || 3));
const CIRCUIT_NOGAS_SPIKE = Math.max(1, Number(process.env.CASHFLOW_CIRCUIT_NOGAS_SPIKE || 3));
const CIRCUIT_MIN_SUCCESS_RATE = clamp(Number(process.env.CASHFLOW_CIRCUIT_MIN_SUCCESS_RATE || 0.7), 0.1, 0.99);
const CIRCUIT_MIN_ATTEMPTS = Math.max(1, Number(process.env.CASHFLOW_CIRCUIT_MIN_ATTEMPTS || 4));
const CIRCUIT_REINVEST_BPS = Math.round(clamp(Number(process.env.CASHFLOW_CIRCUIT_REINVEST_BPS || 7000), 2000, 8500));

const RAILWAY_TOKEN = process.env.RAILWAY_TOKEN || '';
const RAILWAY_PROJECT_ID = process.env.RAILWAY_PROJECT_ID || '';
const RAILWAY_SERVICE_ID = process.env.RAILWAY_SERVICE_ID || '';
const RAILWAY_ENVIRONMENT_ID = process.env.RAILWAY_ENVIRONMENT_ID || '';

const NETWORKS = ['ETH_MAINNET', 'OPT_MAINNET', 'ARB_MAINNET', 'POLYGON_MAINNET'];

const defaults = {
  ETH_MAINNET: { minPayoutEth: 0.003, gasMultiplier: 6, reinvestBps: 3500 },
  OPT_MAINNET: { minPayoutEth: 0.0012, gasMultiplier: 4, reinvestBps: 2800 },
  ARB_MAINNET: { minPayoutEth: 0.0015, gasMultiplier: 5, reinvestBps: 3000 },
  POLYGON_MAINNET: { minPayoutEth: 0.00035, gasMultiplier: 2, reinvestBps: 2000 },
};

const floors = {
  ETH_MAINNET: 0.0018,
  OPT_MAINNET: 0.0005,
  ARB_MAINNET: 0.0006,
  POLYGON_MAINNET: 0.00012,
};

const ceilings = {
  ETH_MAINNET: 0.02,
  OPT_MAINNET: 0.01,
  ARB_MAINNET: 0.01,
  POLYGON_MAINNET: 0.005,
};

const reserveDefaults = {
  ETH_MAINNET: 0.01,
  OPT_MAINNET: 0.003,
  ARB_MAINNET: 0.004,
  POLYGON_MAINNET: 0.0008,
};

const topupThresholdDefaults = {
  ETH_MAINNET: 0.01,
  OPT_MAINNET: 0.003,
  ARB_MAINNET: 0.003,
  POLYGON_MAINNET: 0.0008,
};

const topupAmountDefaults = {
  ETH_MAINNET: 0.01,
  OPT_MAINNET: 0.003,
  ARB_MAINNET: 0.003,
  POLYGON_MAINNET: 0.001,
};

function clamp(value, min, max) {
  return Math.min(max, Math.max(min, value));
}

function writePatchFile(updates) {
  const abs = path.resolve(process.cwd(), OUTPUT_PATCH_FILE);
  const lines = [
    '# Auto-generated by scripts/cashflow-autotune.js',
    `# Generated at ${new Date().toISOString()}`,
    '# Apply selectively to your deployment environment',
    '',
  ];

  for (const item of updates) {
    lines.push(`${item.key}=${item.value}`);
  }

  fs.mkdirSync(path.dirname(abs), { recursive: true });
  const tmp = abs + '.tmp';
  fs.writeFileSync(tmp, `${lines.join('\n')}\n`, 'utf8');
  fs.renameSync(tmp, abs);
  return abs;
}

function roundEth(value) {
  return Number(value.toFixed(8));
}

function parseEnvNumber(key, fallback) {
  const raw = process.env[key];
  if (raw === undefined || raw === null || raw === '') return fallback;
  const num = Number(raw);
  return Number.isFinite(num) ? num : fallback;
}

function inferNetworkSuffix(row) {
  const payload = row?.payload || {};
  const botId = String(payload.botId || '').toLowerCase();
  if (botId.startsWith('eth')) return 'ETH_MAINNET';
  if (botId.startsWith('op')) return 'OPT_MAINNET';
  if (botId.startsWith('arb')) return 'ARB_MAINNET';
  if (botId.startsWith('pol') || botId.includes('polygon')) return 'POLYGON_MAINNET';
  return null;
}

function makeStats() {
  return {
    transfer: 0,
    transferFailed: 0,
    skippedThreshold: 0,
    skippedNoGas: 0,
    gasErrors: 0,
  };
}

async function fetchJson(path) {
  const controller = new AbortController();
  const timer = setTimeout(() => controller.abort(), 15000);
  try {
    const response = await fetch(`${APP_BASE_URL}${path}`, {
      headers: { 'content-type': 'application/json' },
      signal: controller.signal,
    });
    if (!response.ok) {
      throw new Error(`Failed ${path}: HTTP ${response.status}`);
    }
    return response.json();
  } finally {
    clearTimeout(timer);
  }
}


function decideTuning(networkSuffix, stats) {
  const base = defaults[networkSuffix];
  const baseReserve = reserveDefaults[networkSuffix];
  const baseTopupThreshold = topupThresholdDefaults[networkSuffix];
  const baseTopupAmount = topupAmountDefaults[networkSuffix];

  let minPayoutEth = parseEnvNumber(`MIN_PAYOUT_ETH_${networkSuffix}`, base.minPayoutEth);
  let gasMultiplier = Math.round(parseEnvNumber(`MIN_PAYOUT_GAS_MULTIPLIER_${networkSuffix}`, base.gasMultiplier));
  let reinvestBps = Math.round(parseEnvNumber(`SELF_SUSTAIN_REINVEST_BPS_${networkSuffix}`, base.reinvestBps));
  let gasReserveEth = parseEnvNumber(`GAS_RESERVE_ETH_${networkSuffix}`, baseReserve);
  let gasTopupThresholdEth = parseEnvNumber(`GAS_TOPUP_THRESHOLD_${networkSuffix}`, baseTopupThreshold);
  let gasTopupAmountEth = parseEnvNumber(`GAS_TOPUP_AMOUNT_${networkSuffix}`, baseTopupAmount);

  const attempts = stats.transfer + stats.transferFailed;
  const successRate = attempts > 0 ? stats.transfer / attempts : 1;
  const circuitBreakerTriggered = CIRCUIT_BREAKER_ENABLED && (
    stats.transferFailed >= CIRCUIT_FAIL_SPIKE ||
    stats.skippedNoGas >= CIRCUIT_NOGAS_SPIKE ||
    (attempts >= CIRCUIT_MIN_ATTEMPTS && successRate < CIRCUIT_MIN_SUCCESS_RATE)
  );

  if (stats.transfer === 0 && stats.skippedThreshold >= 20 && stats.gasErrors === 0) {
    minPayoutEth *= 0.88;
    gasMultiplier -= 1;
    reinvestBps -= 300;
  }

  if (stats.transferFailed >= 2 || stats.gasErrors >= 1 || stats.skippedNoGas >= 2) {
    minPayoutEth *= 1.15;
    gasMultiplier += 1;
    reinvestBps += 300;
  }

  if (stats.transfer >= 10 && successRate >= 0.95) {
    minPayoutEth *= 0.96;
    reinvestBps -= 100;
  }

  if (circuitBreakerTriggered) {
    minPayoutEth *= 1.25;
    gasMultiplier += 2;
    reinvestBps = Math.max(reinvestBps, CIRCUIT_REINVEST_BPS);
    gasReserveEth *= 1.4;
    gasTopupThresholdEth *= 1.35;
    gasTopupAmountEth *= 1.2;
  } else {
    gasReserveEth *= 0.98;
    gasTopupThresholdEth *= 0.98;
  }

  minPayoutEth = roundEth(clamp(minPayoutEth, floors[networkSuffix], ceilings[networkSuffix]));
  gasMultiplier = clamp(gasMultiplier, 1, 10);
  // ═══ IRONCLAD FLOOR: Owner gets ≥15% of net revenue. Max reinvest = 85% (8500 BPS) ═══
  reinvestBps = clamp(reinvestBps, 1200, 8500);
  gasReserveEth = roundEth(clamp(gasReserveEth, baseReserve * 0.7, baseReserve * 3.0));
  gasTopupThresholdEth = roundEth(clamp(gasTopupThresholdEth, baseTopupThreshold * 0.7, baseTopupThreshold * 3.0));
  gasTopupAmountEth = roundEth(clamp(gasTopupAmountEth, baseTopupAmount * 0.7, baseTopupAmount * 3.0));

  return {
    attempts,
    successRate,
    circuitBreakerTriggered,
    minPayoutEth,
    gasMultiplier,
    reinvestBps,
    gasReserveEth,
    gasTopupThresholdEth,
    gasTopupAmountEth,
  };
}

async function main() {
  const cutoff = Date.now() - LOOKBACK_HOURS * 60 * 60 * 1000;
  const logsPayload = await fetchJson(`/api/alchemy/wallet/logs?limit=${LOG_LIMIT}`);
  const logs = Array.isArray(logsPayload?.logs) ? logsPayload.logs : [];

  const statsByNetwork = Object.fromEntries(NETWORKS.map((name) => [name, makeStats()]));

  for (const row of logs) {
    const ts = Date.parse(row?.time || '');
    if (!Number.isFinite(ts) || ts < cutoff) continue;

    const network = inferNetworkSuffix(row);
    if (!network || !statsByNetwork[network]) continue;

    const stats = statsByNetwork[network];
    if (row?.type === 'transfer' || row?.type === 'transfer_token') stats.transfer += 1;
    if (row?.type === 'transfer_failed' || row?.type === 'transfer_token_failed') stats.transferFailed += 1;
    if (row?.type === 'distribution_skipped_threshold') stats.skippedThreshold += 1;
    if (row?.type === 'distribution_skipped_no_gas') stats.skippedNoGas += 1;
    if (row?.type === 'gas_topup_error' || row?.type === 'gas_check_error') stats.gasErrors += 1;
  }

  const updates = [];

  for (const network of NETWORKS) {
    const stats = statsByNetwork[network];
    const next = decideTuning(network, stats);

    updates.push({ key: `MIN_PAYOUT_ETH_${network}`, value: String(next.minPayoutEth) });
    updates.push({ key: `MIN_PAYOUT_GAS_MULTIPLIER_${network}`, value: String(next.gasMultiplier) });
    updates.push({ key: `SELF_SUSTAIN_REINVEST_BPS_${network}`, value: String(next.reinvestBps) });
    updates.push({ key: `GAS_RESERVE_ETH_${network}`, value: String(next.gasReserveEth) });
    updates.push({ key: `GAS_TOPUP_THRESHOLD_${network}`, value: String(next.gasTopupThresholdEth) });
    updates.push({ key: `GAS_TOPUP_AMOUNT_${network}`, value: String(next.gasTopupAmountEth) });

    console.log(
      `${network}: attempts=${next.attempts} successRate=${(next.successRate * 100).toFixed(1)}% ` +
      `skipped=${stats.skippedThreshold} noGas=${stats.skippedNoGas} gasErrors=${stats.gasErrors} ` +
      `breaker=${next.circuitBreakerTriggered ? 'ON' : 'off'} ` +
      `=> minPayout=${next.minPayoutEth} gasMult=${next.gasMultiplier} reinvestBps=${next.reinvestBps} ` +
      `reserve=${next.gasReserveEth} topupThreshold=${next.gasTopupThresholdEth}`
    );
  }

  const patchPath = writePatchFile(updates);
  console.log(`Wrote tuning patch file: ${patchPath}`);

  const railwayOpts = { token: RAILWAY_TOKEN, projectId: RAILWAY_PROJECT_ID, serviceId: RAILWAY_SERVICE_ID, environmentId: RAILWAY_ENVIRONMENT_ID };
  const hasRailwayCreds = Boolean(RAILWAY_TOKEN) && Boolean(RAILWAY_PROJECT_ID);
  const effectiveDryRun = DRY_RUN || !hasRailwayCreds;

  if (!hasRailwayCreds) {
    console.warn('Railway credentials missing; running in local-output mode (no remote env upsert).');
  }

  for (const update of updates) {
    if (effectiveDryRun) {
      console.log(`[dry-run] ${update.key}=${update.value}`);
      continue;
    }
    await upsertEnvVar(update.key, update.value, railwayOpts);
  }

  console.log(`Applied ${updates.length} cashflow-tuning env updates (${effectiveDryRun ? 'dry-run' : 'production'} mode).`);

  if (AUTO_REDEPLOY && !effectiveDryRun) {
    await triggerRedeployment(railwayOpts);
    console.log('Auto-redeploy requested after cashflow tuning.');
  }
}

main().catch((error) => {
  console.error(error instanceof Error ? error.message : String(error));
  process.exit(1);
});
